package Model.LivingRoomClasses;

public class TileToExtract {

    private int row;
    private int column;



    public TileToExtract( int x, int y){

        this.row = x;
        this.column = y;
    }




    public int getRow() {
        return row;
    }

    public int getColumn() {
        return column;
    }



}
